export function generateQuestions(selectedTopics, totalCount = 10, codingOnly = false) {
  const allQuestions = [];
  const isAnyTopicSelected = selectedTopics && Object.values(selectedTopics).some(v => v);

  if (!isAnyTopicSelected) {
    Object.values(QUESTION_BANK).forEach(qList => {
      allQuestions.push(...qList);
    });
  } else {
    Object.entries(selectedTopics).forEach(([topic, enabled]) => {
      if (enabled && QUESTION_BANK[topic]) {
        allQuestions.push(...QUESTION_BANK[topic]);
      }
    });
  }

  if (allQuestions.length === 0) return [];

  const filtered = codingOnly
    ? allQuestions.filter(q => q.type === 'coding')
    : allQuestions.filter(q => q.type === 'mcq');

  if (filtered.length === 0) return [];

  const shuffled = [...filtered].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(totalCount || 10, shuffled.length));
}

export function generateQuestionsSeeded(selectedTopics, totalCount = 10, codingOnly = false, seed = '') {
  const allQuestions = [];
  const isAnyTopicSelected = selectedTopics && Object.values(selectedTopics).some(v => v);

  if (!isAnyTopicSelected) {
    Object.values(QUESTION_BANK).forEach(qList => {
      allQuestions.push(...qList);
    });
  } else {
    Object.entries(selectedTopics).forEach(([topic, enabled]) => {
      if (enabled && QUESTION_BANK[topic]) {
        allQuestions.push(...QUESTION_BANK[topic]);
      }
    });
  }

  if (allQuestions.length === 0) return [];

  const filtered = codingOnly
    ? allQuestions.filter(q => q.type === 'coding')
    : allQuestions.filter(q => q.type === 'mcq');

  if (filtered.length === 0) return [];

  // Simple Mulberry32 seeded random number generator
  let h = 0;
  for (let i = 0; i < seed.length; i++) {
    h = Math.imul(31, h) + seed.charCodeAt(i) | 0;
  }
  const seededRandom = () => {
    let t = h += 0x6D2B79F5;
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };

  // Seeded shuffle
  const shuffled = [...filtered].sort(() => seededRandom() - 0.5);
  return shuffled.slice(0, Math.min(totalCount || 10, shuffled.length));
}

